# Официальный порядок стилей (docs/samples: "1. АШИХАРА – А, 2. КОСИКИ – Б,
# ..."). Используется, чтобы сетка/протокол ката шли стиль за стилем в этом
# порядке, а не вперемешку.
KATA_STYLE_ORDER = list(KATA_TYPES.keys())

# Обратный индекс "название конкретной ката -> стиль". Некоторые названия
# повторяются в разных стилях (напр. "Сэйсан" есть в Косики, Годзю-рю и
# Сито-рю) - при совпадении побеждает первый стиль по официальному порядку
# выше, т.к. setdefault не перезаписывает уже найденное соответствие.
_KATA_NAME_TO_STYLE = {}
for _style, _entries in KATA_TYPES.items():
    for _code, _name, _coefficient in _entries:
        _KATA_NAME_TO_STYLE.setdefault(_name, _style)


def kata_style(category_name):
    """Стиль (Ашихара/Косики/...) конкретной ката по названию, которое
    спортсмен выбрал при заявке (Registration.category_name). Сетка и
    протокол ката группируются по стилю, а не по конкретной ката - так же,
    как в официальных протоколах (docs/samples), где одна сетка/страница
    покрывает весь стиль целиком. Название, которого нет в реестре,
    возвращается как есть - это даёт стабильную (хоть и отдельную) группу
    вместо падения на нестандартных данных."""
    if not category_name:
        return category_name
    return _KATA_NAME_TO_STYLE.get(category_name, category_name)
